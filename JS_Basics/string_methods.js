// String methods in JavaScript:

let myString = "Sally sells sea shells on the sea shore";

console.log(myString.length);
console.log(myString.indexOf("shore"));
console.log(myString.lastIndexOf("s"));

// slicing the string:
console.log(myString.slice(3, 16));    // (start index, end index)

// replace: 
console.log(myString.replace("Sally", "Shruti"));

// or:
d = d.replace("shells", "snake");
console.log(d, myString);
